print("Ejercicio 11:", resultado)

"""**Ejercicio 11**

El patrón es `\b(\w)\w*\1\b` y es un ejemplo clásico del uso de backreferences.

El `\b` inicial ancla el inicio en un límite de palabra. Luego `(\w)` es un grupo capturante, marcado por los paréntesis sin `?:`. Captura exactamente un carácter de palabra, que puede ser una letra o un dígito, y lo guarda en la memoria del motor bajo el nombre de grupo 1. Esta es la primera letra de la palabra.

A continuación `\w*` coincide con cero o más caracteres de palabra. Esto cubre todas las letras intermedias de la palabra. El `*` permite que sea cero, lo que en teoría permitiría palabras de una sola letra, aunque en ese caso no habría letras intermedias y la primera y última serían la misma.

La parte más interesante es `\1`. Esto es una backreference al grupo 1, es decir, le dice al motor que en esa posición debe aparecer exactamente el mismo texto que se capturó entre los primeros paréntesis. Si la primera letra fue `o`, entonces `\1` exige que la última letra también sea `o`. Si fue `e`, exige `e`. Es como si el patrón se reescribiera dinámicamente según lo que encontró al principio.

Aplicado al texto `"oso casa pera elefante nivel"`, el motor evalúa cada palabra. En `oso` captura `o`, recorre `s` con `\w*`, y verifica que la última letra sea `o`, lo cual se cumple. En `elefante` captura `e`, recorre `lefant` con `\w*`, y verifica que termine en `e`, lo cual también se cumple. Las demás palabras no pasan la verificación.

Sin embargo el resultado impreso es `['o', 'e']` en lugar de `['oso', 'elefante']`. Esto ocurre por un comportamiento específico de `re.findall()`: cuando el patrón contiene grupos de captura, la función no devuelve las coincidencias completas sino únicamente el contenido de los grupos. Como el único grupo capturante es `(\w)`, que guarda solo la primera letra, eso es exactamente lo que regresa. La coincidencia completa existe internamente en el motor pero `findall` la descarta. Para recuperar la palabra entera habría que envolver todo el patrón en un grupo adicional o usar `re.finditer()` y acceder a `match.group(0)`.
"""

