"""**Ejercicio 7**

El patrón completo es `\b(?:[aeiouAEIOU][^aeiouAEIOU]*){3,}\b`. Para entenderlo hay que descomponerlo en partes.

Los `\b` al inicio y al final son anclajes de límite de palabra. Le dicen al motor que la coincidencia debe comenzar y terminar en el borde entre un carácter de palabra y uno que no lo es, como un espacio o un signo de puntuación.

El núcleo del patrón es el grupo `(?:[aeiouAEIOU][^aeiouAEIOU]*)`. El `?:` indica que es un grupo no capturante, es decir, agrupa la expresión para aplicarle un cuantificador pero no guarda lo que encuentra. Dentro del grupo, `[aeiouAEIOU]` es una clase de caracteres que coincide con exactamente una vocal, ya sea mayúscula o minúscula. Inmediatamente después, `[^aeiouAEIOU]*` es otra clase de caracteres pero negada con `^`, lo que significa que coincide con cualquier carácter que no sea una vocal. El `*` permite que aparezca cero o más veces. Entonces el grupo completo representa una vocal seguida de cualquier cantidad de no-vocales, que en una palabra normal serían consonantes.

El cuantificador `{3,}` aplicado al grupo le exige que ese patrón ocurra al menos 3 veces seguidas, lo que equivale a pedir que la palabra tenga al menos 3 vocales.

El problema está en que `[^aeiouAEIOU]*` no distingue entre consonantes y espacios. Ambos son "no-vocales". Entonces cuando el motor termina de procesar una palabra y encuentra un espacio, lo consume como parte del patrón porque el espacio satisface `[^aeiouAEIOU]*`, y sigue buscando la siguiente vocal en la palabra siguiente. Así cruza todas las palabras del texto sin detenerse y devuelve `'es un lenguaje de programación poderoso'` como una sola coincidencia gigante en lugar de palabras individuales.
"""

